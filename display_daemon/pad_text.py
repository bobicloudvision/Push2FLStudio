_GAP = 1            # blank columns between glyphs
_SPACE = 2          # blank columns for a space
_TAIL = 8           # trailing blanks so the loop has breathing room


def _char_columns(ch: str, font, height: int):
    rows = font.get(ch.upper())
    if rows is None:
        return [(False,) * height] * _SPACE
    width = len(rows[0])
    return [tuple(rows[r][j] == "#" for r in range(height)) for j in range(width)]


def _build_columns(text: str, font, height: int):
    cols = []
    blank = (False,) * height
    for ch in text:
        if ch == " ":
            cols += [blank] * _SPACE
        else:
            cols += _char_columns(ch, font, height)
            cols += [blank] * _GAP
    cols += [blank] * _TAIL
    return cols


class PadText:
    """Scrolls *text* across the 8x8 pads in a single *color*."""

    def __init__(self, text: str, speed: int = 3,
                 color: int = push2_midi.WHITE, big: bool = False) -> None:
        font = FONT_BIG if big else FONT_SMALL
        self.height = 7 if big else 5
        self.color = color
        self.speed = max(1, speed)          # frames per column step
        self.columns = _build_columns(text, font, self.height)
        self.total = len(self.columns)
        # Vertically center the glyph: top pad row of glyph row 0.
        self.top_row = 7 - (8 - self.height) // 2

    def render(self, pads, frame: int, held: set) -> None:
        scroll = (frame // self.speed) % self.total
        for c in range(push2_midi.PAD_COLS):          # pad columns, left->right
            column = self.columns[(scroll + c) % self.total]
            for pr in range(push2_midi.PAD_ROWS):     # pad rows, bottom->top
                note = push2_midi.pad_note(pr, c)
                if note in held:
                    continue
                r = self.top_row - pr                 # glyph row for this pad
                lit = 0 <= r < self.height and column[r]
                pads.set_pad(note, self.color if lit else push2_midi.OFF)
